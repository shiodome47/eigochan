export function findPhraseById(id: string): Phrase | undefined {
  if (isCustomPhrase(id)) {
    return loadCustomPhrases().find((p) => p.id === id);
  }
  return PHRASES.find((p) => p.id === id);
}

/** 固定フレーズと自作フレーズを合成して返す。自作はあとに並ぶ。 */
export function getAllPhrases(): Phrase[] {
  return [...PHRASES, ...loadCustomPhrases()];
}
